export const seatMap: Seat[] = Array.from({ length: 18 }, (_, index) => {
  const row = Math.floor(index / 6) + 1;
  const column = ["A", "B", "C", "D", "E", "F"][index % 6];
  const premium = row === 1;
  return {
    id: `${row}${column}`,
    row,
    column,
    type: premium ? "premium" : "standard",
    status: ["1C", "2B", "2E"].includes(`${row}${column}`) ? "occupied" : "available",
    price: premium ? 55 : 18,
  };
});
